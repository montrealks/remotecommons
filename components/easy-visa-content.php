        <h2 class="h4 mb-3">Key Features</h2>
        <ul class="lh-lg mb-4">
            <li><strong>Validity:</strong> 5 years, multi-entry</li>
            <li><strong>Stay limit:</strong> 180 days per country, per entry</li>
            <li><strong>Requirements:</strong> ≥ 600,000 THB (≈ USD 15,000) in savings and proof of remote work</li>
            <li><strong>Work rights:</strong> Remote work for foreign employers only—no local employment</li>
            <li><strong>Application:</strong> Single digital portal serving all four countries</li>
        </ul>

        <h2 class="h4 mb-3">What It Means</h2>
        <p class="lh-lg mb-4">
